function buildCloudinaryUrl(publicId: string) {
  return `https://res.cloudinary.com/${cloudinaryCloudName}/image/upload/${publicId}.jpg`;
}

function makeImages(
  vehicleId: string,
  title: string,
  asset: (typeof vehicleAssets)[keyof typeof vehicleAssets],
): VehicleImage[] {
  return asset.map((publicId, index) => ({
    id: `${vehicleId}-image-${index + 1}`,
    vehicleId,
    imageUrl: buildCloudinaryUrl(publicId),
    altText: `${title} photo ${index + 1}`,
    sortOrder: index,
    isHero: index === 0,
    cloudinaryPublicId: publicId,
    createdAt: now,
  }));
}

function makeVehicle(
  partial: Omit<Vehicle, "location" | "images" | "heroImageUrl"> & {
    assetKey: keyof typeof vehicleAssets;
  },
): Vehicle {
  const images = makeImages(
    partial.id,
    partial.title,
    vehicleAssets[partial.assetKey],
  );
  const location = locations.find((item) => item.id === partial.locationId) || null;

  return {
    ...partial,
    images,
    heroImageUrl: images[0]?.imageUrl ?? null,
    location,
  };
}

